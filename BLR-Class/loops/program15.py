#program to check whether the string is in lower case or not
def islower(s):
    for i in s:
        if i>'A' and i<'Z':
            return False
    return True
print(islower('helloo  '))



#program to check whether the string is in upper case or not
def islower(s):
    for i in s:
        if i>'a' and i<'z':
            return False
    return True
print(islower('HGDFJKASGBK  3456789 '))